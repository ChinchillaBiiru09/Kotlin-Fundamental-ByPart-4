package PRO1_DoWhileLoops

fun main() {
    //DO WHILE LOOPS
//    sama dengan while loops, yang membedakan adalah while loops akan ditampilkan terlenih dahulu
//    baru dicek kondisinya, sehingga jika kondisinya salah, progam akan dijalakan 1x
//    do{
//      Statement/code program
//    }while(kondisi)
    var i = 11
    do{
        println("Do while loops ke-$i")
        i++
    } while (i < 10) //meskipun kondisi salah, akan tetap dijalankan 1x
}

